    SUM(CASE WHEN TRANSACTION_ID IS NULL THEN 1 ELSE 0 END) AS NULL_TXN_ID,
    SUM(CASE WHEN AMOUNT IS NULL THEN 1 ELSE 0 END) AS NULL_AMOUNT,
