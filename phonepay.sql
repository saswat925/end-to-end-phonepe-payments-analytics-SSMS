------Users Table
SELECT
    SUM(CASE WHEN USER_ID IS NULL THEN 1 ELSE 0 END) AS NULL_USER_ID,
    SUM(CASE WHEN USER_NAME IS NULL THEN 1 ELSE 0 END) AS NULL_NAME,
    SUM(CASE WHEN AGE IS NULL THEN 1 ELSE 0 END) AS NULL_AGE,
