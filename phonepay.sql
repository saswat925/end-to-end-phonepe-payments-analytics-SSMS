--DIM_USERS
CREATE TABLE DIM_USERS (
    USER_KEY INT IDENTITY(1,1) PRIMARY KEY,
    USER_ID VARCHAR(100) UNIQUE,
    USER_NAME VARCHAR(200),
    AGE INT,
    JOIN_DATE DATE
);  truncate table dim_users
--load data
INSERT INTO DIM_USERS select * from users_clean;

---DIM_DATE
CREATE TABLE DIM_DATE (
    DATE_KEY INT PRIMARY KEY,
    FULL_DATE DATE,
    YEAR INT,
    QUARTER INT,
    MONTH_NO INT,
    MONTH_NAME VARCHAR(20),
    DAY_NO INT
);
--load data 
INSERT INTO DIM_DATE
SELECT DISTINCT
    CAST(FORMAT(TRANSACTION_DATE,'yyyyMMdd') AS INT),
    TRANSACTION_DATE,
    YEAR(TRANSACTION_DATE),
    DATEPART(QUARTER,TRANSACTION_DATE),
    MONTH(TRANSACTION_DATE),
    DATENAME(MONTH,TRANSACTION_DATE),
    DAY(TRANSACTION_DATE)
FROM TRANSACTIONS_CLEAN;

--FACT_TRANSACTIONS
CREATE TABLE FACT_TRANSACTIONS (
    TRANSACTION_ID VARCHAR(100) PRIMARY KEY,
    USER_KEY INT,
    DATE_KEY INT,
    AMOUNT DECIMAL(18,2),
    SERVICE VARCHAR(100),
    SERVICE_TYPE VARCHAR(100),
    PAYMENT_STATUS VARCHAR(50),
    REASON VARCHAR(255)
);    
--load data
INSERT INTO FACT_TRANSACTIONS
 SELECT
    T.TRANSACTION_ID,
    D.USER_KEY,
    CAST(FORMAT(T.TRANSACTION_DATE,'yyyyMMdd') AS INT),
    T.AMOUNT,
    T.SERVICE,
    T.SERVICE_TYPE,
    T.PAYMENT_STATUS,
    T.REASON
FROM TRANSACTIONS_CLEAN T
JOIN DIM_USERS D
ON T.USER_ID = D.USER_ID;  


